<?php declare(strict_types=1);

use Winkbrace\Advent2025\Day07\QuantumTracer;
use Winkbrace\Advent2025\InputReader;

beforeEach(function() {
    $input = InputReader::read('07', 'example');
    $this->tracer = new QuantumTracer($input);
});

it('should draw the timelines', function () {
    $this->tracer->traceForSmallSampleDrawing();

    expect($this->tracer->draw(0))->toBe(
        <<<DRAW
        .......S.......
        .......|.......
        ......|^.......
        ......|........
        .....|^.^......
        .....|.........
        ....|^.^.^.....
        ....|..........
        ...|^.^...^....
        ...|...........
        ..|^.^...^.^...
        ..|............
        .|^...^.....^..
        .|.............
        |^.^.^.^.^...^.
        |..............
        DRAW
    )->and($this->tracer->draw(1))->toBe(
        <<<DRAW
        .......S.......
        .......|.......
        ......|^.......
        ......|........
        .....|^.^......
        .....|.........
        ....|^.^.^.....
        ....|..........
        ...|^.^...^....
        ...|...........
        ..|^.^...^.^...
        ..|............
        .|^...^.....^..
        .|.............
        .^|^.^.^.^...^.
        ..|............
        DRAW
    );
});

it('should count the timelines', function () {
    $this->tracer->traceForSmallSampleDrawing();

    expect($this->tracer->timelines())->toHaveCount(40);
});

it('should count the timelines optimally', function () {
    $this->tracer->trace();

    expect($this->tracer->sumTimelineCounts())->toBe(40)
